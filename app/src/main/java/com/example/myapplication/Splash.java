package com.example.myapplication;

public class Splash {
    private String Splash_Message;
    private String Splash_Background;
    private int Server_On;

    public int getServer_On() {
        return Server_On;
    }

    public void setServer_On(int server_On) {
        Server_On = server_On;
    }

    public String getSplash_Message() {
        return Splash_Message;
    }

    public void setSplash_Message(String splash_Message) {
        Splash_Message = splash_Message;
    }

    public String getSplash_Background() {
        return Splash_Background;
    }

    public void setSplash_Background(String splash_Background) {
        Splash_Background = splash_Background;
    }
}
